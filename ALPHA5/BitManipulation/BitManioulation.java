package BitManipulation;

public class BitManioulation {
    public static void main(String[] args) {
        System.out.println("AND: " + (2 & 1));
        System.out.println("OR: " + (2 | 1));
        System.out.println("XOR: " + (2 ^ 1));
        System.out.println("NOT: " + (~2));
        System.out.println("Left Shift: " + (2 << 1));
        System.out.println("Right Shift: " + (2 >> 1));
        
    }
}
